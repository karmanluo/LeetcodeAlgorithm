package Test;

public class TESTLOWBIT {
    public static void main(String[] args) {
        System.out.println(Integer.toBinaryString(-6));

        System.out.println(" " + Integer.toBinaryString(Integer.MAX_VALUE) + "--->" + "Integer.MAX_VALUE : " + Integer.MAX_VALUE);
        System.out.println(Integer.toBinaryString(Integer.MIN_VALUE) + "--->" + "Integer.MIN_VALUE :" + Integer.MIN_VALUE);
        System.out.println(Integer.toBinaryString(-1) + "--->" + -1);
    }
}
